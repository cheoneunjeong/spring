package com.example.spring.domain;

public class SAnswer {

	private String a;
	private int s_num;
	private String q;
	private String t;
	private int ans_num;
	
	
	public String getA() {
		return a;
	}
	public void setA(String a) {
		this.a = a;
	}

	public String getQ() {
		return q;
	}
	public void setQ(String q) {
		this.q = q;
	}
	public String getT() {
		return t;
	}
	public void setT(String t) {
		this.t = t;
	}
	public int getAns_num() {
		return ans_num;
	}
	public void setAns_num(int ans_num) {
		this.ans_num = ans_num;
	}
	public int getS_num() {
		return s_num;
	}
	public void setS_num(int s_num) {
		this.s_num = s_num;
	}

}
